C     SQUARE ROOT PROGRAM
C
      DOUBLE PRECISION NUMBER, ROOT
C
      DO 500 I = 1, 3
C
    5 WRITE (6, 10)
   10 FORMAT ('ENTER A NUMBER')
C
      READ (5,100) NUMBER
  100 FORMAT (F5.1)
C
      IF (NUMBER .GE. 0.0) GO TO 175
      WRITE (6, 150)
  150 FORMAT ('*** THE NUMBER MUST NOT BE NEGATIVE.')
      GOTO 5
C
  175 ROOT = DSQRT(NUMBER)
C
      WRITE (6,200) NUMBER, ROOT
  200 FORMAT ('THE SQUARE ROOT OF ', F10.1, ' IS ', F15.10)
C
  500 CONTINUE
C
      PAUSE
      STOP
      END
